#include "ft_printf.h"

void	ft_putnbr_fd(int n)
{
	if (n == -2147483648)
	{
		ft_putchar_fd('-');
		ft_putchar_fd('2');
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10);
	}
	ft_putchar_fd((n % 10) + '0');
}